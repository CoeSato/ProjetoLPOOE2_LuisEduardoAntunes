    public void listarBibliotecarios(){
        cmbBibliotecarios.removeAllItems();
        
        jpa = new PersistenciaJPA();
        jpa.conexaoAberta();
        List<Bibliotecario> lista = jpa.getBibliotecarios("");
        for(Bibliotecario o: lista){
            cmbBibliotecarios.addItem(o);
        }
        jpa.fecharConexao();
    }
    
    public void listarClientes(){
        cmbClientes.removeAllItems();
        
        jpa = new PersistenciaJPA();
        jpa.conexaoAberta();
        List<Cliente> lista = jpa.getClientes("");
        for(Cliente o: lista){
            cmbClientes.addItem(o);
        }
        jpa.fecharConexao();
    }

