    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Criando nova modalidade
        try {

            jpa = new PersistenciaJPA();
            jpa.conexaoAberta();

            // Busque a entidade gerenciada
            Reserva reservaGerenciado = null;
            Reserva r1 = new Reserva();
            
            if (reserva != null && reserva.getId() != 0) {
                reservaGerenciado = (Reserva) jpa.find(Reserva.class, reserva.getId());
            }

            if (reservaGerenciado == null) {
                reservaGerenciado = new Reserva();
            }
            
            reservaGerenciado.setLivroReservado(txtLivro.getText());
            reservaGerenciado.setCliente((Cliente)cmbClientes.getSelectedItem());
            reservaGerenciado.setBibliotecario((Bibliotecario)cmbBibliotecarios.getSelectedItem());
            
            r1.setLivroReservado(txtDataInicio.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date date = sdf.parse(r1.getLivroReservado());
            Calendar calendar = Calendar.getInstance();
            calendar.setTime(date);
            reservaGerenciado.setDataInicio(calendar);
            
            r1.setLivroReservado(txtDataFim.getText());
            date = sdf.parse(r1.getLivroReservado());
            calendar = Calendar.getInstance();
            calendar.setTime(date);
            reservaGerenciado.setDataFim(calendar);
            

            jpa.merge(reservaGerenciado);
            JOptionPane.showMessageDialog(this, "Reserva salvo com sucesso!");
            dispose();
        } catch (Exception ex) {
            Logger.getLogger(TelaCadastroReserva.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            jpa.fecharConexao();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

