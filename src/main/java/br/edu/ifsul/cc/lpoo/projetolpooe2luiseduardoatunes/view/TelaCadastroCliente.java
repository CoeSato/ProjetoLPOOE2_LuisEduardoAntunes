    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {

            jpa = new PersistenciaJPA();
            jpa.conexaoAberta();

            // Busque a entidade gerenciada
            Cliente clienteGerenciado = null;
            if (cliente != null && cliente.getId() != 0) {
                clienteGerenciado = (Cliente) jpa.find(Cliente.class, cliente.getId());
            }

            if (clienteGerenciado == null) {
                clienteGerenciado = new Cliente();
            }
            clienteGerenciado.setNome(txtNome.getText());
            clienteGerenciado.setEndereco(txtEndereco.getText());
            clienteGerenciado.setEmail(txtEmail.getText());
            clienteGerenciado.setTelefone(txtTelefone.getText());

            jpa.merge(clienteGerenciado);
            JOptionPane.showMessageDialog(this, "Cliente salvo com sucesso!");
            dispose();
        } catch (Exception ex) {
            Logger.getLogger(TelaCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            jpa.fecharConexao();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

