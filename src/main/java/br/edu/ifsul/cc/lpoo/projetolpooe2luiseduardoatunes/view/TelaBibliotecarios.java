    private void txtBuscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaKeyReleased
        // TODO add your handling code here:
        String textoBusca = txtBusca.getText().trim();
        
        if(textoBusca.isEmpty()){
            listarBibliotecarios();
            return;
        }
        
        lstBibliotecarios.clearSelection();
        jpa = new PersistenciaJPA();
        jpa.conexaoAberta();
        List<Bibliotecario> lista = jpa.getBibliotecarios(textoBusca);
        System.out.println("Lista: " + lista);
        DefaultListModel modeloLista = new DefaultListModel<>();
        for (Bibliotecario b : lista) {
            modeloLista.addElement(b);
        }
        lstBibliotecarios.setModel(modeloLista);
        jpa.fecharConexao();
    }//GEN-LAST:event_txtBuscaKeyReleased

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        Bibliotecario bibliotecarioSelecionado = lstBibliotecarios.getSelectedValue();
        if (bibliotecarioSelecionado != null) {
            int confirmacaoDel = JOptionPane.showConfirmDialog(rootPane,
                    "Tem certeza que deseja remover bibliotecario " + bibliotecarioSelecionado.getNome()+"?");
            if (confirmacaoDel == JOptionPane.YES_OPTION) {
                try {
                    jpa = new PersistenciaJPA();
                    jpa.conexaoAberta();
                    jpa.remover(bibliotecarioSelecionado);
                    jpa.fecharConexao();
                    listarBibliotecarios();

                } catch (Exception e) {
                    System.err.println("Erro ao excluir bibliotecario: " + e.getMessage());
                } finally {
                    jpa.fecharConexao();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Nenhum bibliotecario selecionado");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        Bibliotecario bibliotecarioSelecionado = lstBibliotecarios.getSelectedValue();
        if (bibliotecarioSelecionado != null) {
            try {
                jpa = new PersistenciaJPA();
                jpa.conexaoAberta();
                TelaCadastroBibliotecario telaCadastro = new TelaCadastroBibliotecario(this, true);
                telaCadastro.setBibliotecario(bibliotecarioSelecionado);
                telaCadastro.setVisible(true);
                listarBibliotecarios();
            } catch (Exception e) {
                System.err.println("Erro ao editar bibliotecario: " + e.getMessage());
            } finally {
                jpa.fecharConexao();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um bibliotecario para editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarActionPerformed
        // TODO add your handling code here:
        Bibliotecario bibliotecarioSelecionado = lstBibliotecarios.getSelectedValue();
        if (bibliotecarioSelecionado != null) {
            Calendar calendar = bibliotecarioSelecionado.getDataAdmissao();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            String formattedDate = sdf.format(calendar.getTime());
            
            JOptionPane.showMessageDialog(rootPane, "Nome: "+bibliotecarioSelecionado.getNome()+"\n"+
                    "Telefone: "+bibliotecarioSelecionado.getTelefone()+"\n"+
                    "Sal√°rio: "+bibliotecarioSelecionado.getSalario()+"\n"+
                    "Data de admiss√£o: "+formattedDate+"\n");
                    
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para visualizar");
        }
    }//GEN-LAST:event_btnVisualizarActionPerformed

