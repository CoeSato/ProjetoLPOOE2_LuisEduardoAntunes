    public void listarReservas(){
        lstReservas.clearSelection();
        jpa = new PersistenciaJPA();
        jpa.conexaoAberta();
        List<Reserva> lista = jpa.getReservas("Lista: ");
        System.out.println(lista);
        DefaultListModel modeloLista = new DefaultListModel<>();
        for (Reserva m : lista) {
            modeloLista.addElement(m);
        }
        lstReservas.setModel(modeloLista);
        jpa.fecharConexao();
    }

