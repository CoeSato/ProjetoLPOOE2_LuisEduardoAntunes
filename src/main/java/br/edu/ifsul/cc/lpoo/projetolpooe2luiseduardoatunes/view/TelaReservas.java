    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        Reserva r = new Reserva();
        
        try {
                jpa = new PersistenciaJPA();
                jpa.conexaoAberta();
                TelaCadastroReserva telaCadastro = new TelaCadastroReserva(this, true);
                telaCadastro.setReserva(r);
                telaCadastro.setVisible(true);
                listarReservas();
            } catch (Exception e) {
                System.err.println("Erro ao adicionar reserva: " + e.getMessage());
            } finally {
                jpa.fecharConexao();
            }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnEdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdicionarActionPerformed
        // TODO add your handling code here:
        Reserva reservaSelecionada = lstReservas.getSelectedValue();
        if (reservaSelecionada != null) {
            try {
                jpa = new PersistenciaJPA();
                jpa.conexaoAberta();
                TelaCadastroReserva telaCadastro = new TelaCadastroReserva(this, true);
                telaCadastro.setReserva(reservaSelecionada);
                telaCadastro.setVisible(true);
                listarReservas();
            } catch (Exception e) {
                System.err.println("Erro ao editar reserva: " + e.getMessage());
            } finally {
                jpa.fecharConexao();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma reserva para editar");
        }
    }//GEN-LAST:event_btnEdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        Reserva reservaSelecionada = lstReservas.getSelectedValue();
        if (reservaSelecionada != null) {
            int confirmacaoDel = JOptionPane.showConfirmDialog(rootPane,
                    "Tem certeza que deseja remover a reserva do livro " + reservaSelecionada.getLivroReservado()+"?");
            if (confirmacaoDel == JOptionPane.YES_OPTION) {
                try {
                    jpa = new PersistenciaJPA();
                    jpa.conexaoAberta();
                    jpa.remover(reservaSelecionada);
                    jpa.fecharConexao();
                    listarReservas();

                } catch (Exception e) {
                    System.err.println("Erro ao excluir reserva: " + e.getMessage());
                } finally {
                    jpa.fecharConexao();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Nenhuma reserva selecionado");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarActionPerformed
        // TODO add your handling code here:
        Reserva reservaSelecionada = lstReservas.getSelectedValue();
        if (reservaSelecionada != null) {
            Calendar calendar = reservaSelecionada.getDataInicio();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            String formattedDate = sdf.format(calendar.getTime());
            
            calendar = reservaSelecionada.getDataFim();
            sdf = new SimpleDateFormat("dd/MM/yyyy");
            String formattedDate1 = sdf.format(calendar.getTime());
            
            JOptionPane.showMessageDialog(rootPane, "Livro reservado: "+reservaSelecionada.getLivroReservado()+"\n"+
                    "Cliente: "+reservaSelecionada.getCliente().getNome()+"\n"+
                    "Bibliotecario: "+reservaSelecionada.getBibliotecario().getNome()+"\n"+
                    "Data inicial da reserva: "+formattedDate+"\n"+
                    "Data final da reserva: "+formattedDate1);
                    
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para visualizar");
        }
    }//GEN-LAST:event_btnVisualizarActionPerformed

    private void txtBuscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaKeyReleased
        // TODO add your handling code here:
        String textoBusca = txtBusca.getText().trim();
        
        if(textoBusca.isEmpty()){
            listarReservas();
            return;
        }
        
        lstReservas.clearSelection();
        jpa = new PersistenciaJPA();
        jpa.conexaoAberta();
        List<Reserva> lista = jpa.getReservas(textoBusca);
        System.out.println("Lista: " + lista);
        DefaultListModel modeloLista = new DefaultListModel<>();
        for (Reserva b : lista) {
            modeloLista.addElement(b);
        }
        lstReservas.setModel(modeloLista);
        jpa.fecharConexao();
    }//GEN-LAST:event_txtBuscaKeyReleased

